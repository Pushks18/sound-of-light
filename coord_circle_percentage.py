def parse_rows(text: str):
    rows = []
    for line in text.splitlines():
        ts_part, coord_part = line.split("\t")
        timestamp = datetime.strptime(ts_part.strip(), "%m/%d/%Y %H:%M:%S")
        x, y, z = [float(v.strip()) for v in coord_part.split(",")]
        rows.append((timestamp, (x, y, z)))
    return rows


def cluster_coords(coords, tol=1.0):
    """
    Group coordinates if each axis is within +-tol of cluster center.
    """
    clusters = []  # each cluster: {"center": [x, y, z], "count": int}
    labels = []

    for point in coords:
        assigned = False
        for i, cluster in enumerate(clusters):
            cx, cy, cz = cluster["center"]
            if (
                abs(point[0] - cx) <= tol
                and abs(point[1] - cy) <= tol
                and abs(point[2] - cz) <= tol
            ):
                # Update center as running mean to keep cluster representative.
                n = cluster["count"]
                cluster["center"][0] = (cx * n + point[0]) / (n + 1)
                cluster["center"][1] = (cy * n + point[1]) / (n + 1)
                cluster["center"][2] = (cz * n + point[2]) / (n + 1)
                cluster["count"] += 1
                labels.append(i)
                assigned = True
                break

        if not assigned:
            clusters.append({"center": [point[0], point[1], point[2]], "count": 1})
            labels.append(len(clusters) - 1)

    return labels, clusters


def main():
    rows = parse_rows(RAW_DATA)
    coords = [coord for _, coord in rows]

    labels, clusters = cluster_coords(coords, tol=1.0)
    counts = Counter(labels)

    sorted_groups = sorted(counts.items(), key=lambda x: x[1], reverse=True)
    top_a_idx, top_a_count = sorted_groups[0]
    top_b_idx, top_b_count = sorted_groups[1]
    other_count = sum(count for _, count in sorted_groups[2:])

    ax, ay, az = clusters[top_a_idx]["center"]
    bx, by, bz = clusters[top_b_idx]["center"]

    pie_sizes = [top_a_count, top_b_count, other_count]
    pie_labels = [
        f"Trap A\n({ax:.1f}, {ay:.1f}, {az:.1f})",
        f"Trap B\n({bx:.1f}, {by:.1f}, {bz:.1f})",
        "Other traps",
    ]

    plt.figure(figsize=(7, 7))
    plt.pie(
        pie_sizes,
        labels=pie_labels,
        autopct="%1.1f%%",
        startangle=90,
    )
    plt.title("Death Causes by Trap (Top 2 + Others, tol = ±1 per axis)")
    plt.axis("equal")
    plt.tight_layout()
    backend = plt.get_backend().lower()
    if "agg" in backend:
        output_file = "coord_circle_percentage.png"
        plt.savefig(output_file, dpi=150)
        print(f"Saved chart to {output_file} (non-interactive backend: {backend})")
    else:
        plt.show()


if __name__ == "__main__":
    main()
